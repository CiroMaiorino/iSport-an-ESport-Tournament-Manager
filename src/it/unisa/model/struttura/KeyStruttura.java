package it.unisa.model.struttura;

public final class KeyStruttura {

	private String CAP,indirizzo;
	
	public KeyStruttura(String CAP,String indirizzo) {
		this.CAP=CAP;
		this.indirizzo=indirizzo;
	}

	public String getCAP() {
		return CAP;
	}

	public String getIndirizzo() {
		return indirizzo;
	}

}
